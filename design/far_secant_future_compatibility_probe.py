def atomic_write_json(output, result):
    output = Path(output)
    output.parent.mkdir(parents=True, exist_ok=True)
    descriptor, temporary = tempfile.mkstemp(
        prefix=output.name + ".", suffix=".tmp", dir=output.parent
    )
    try:
        with os.fdopen(descriptor, "w") as handle:
            json.dump(result, handle, indent=2, sort_keys=True)
            handle.write("\n")
        os.replace(temporary, output)
    except Exception:
        try:
            os.unlink(temporary)
        except FileNotFoundError:
            pass
        raise


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("mode", choices=("estimate", "run"))
    parser.add_argument("--future", type=Path, default=DEFAULT_FUTURE)
    parser.add_argument("--birth", type=Path, default=DEFAULT_BIRTH)
    parser.add_argument("--deep", type=Path, default=DEFAULT_DEEP)
    parser.add_argument("--l9", type=Path, default=DEFAULT_L9)
    parser.add_argument("--output", type=Path)
    args = parser.parse_args()
    if args.mode == "run" and args.output is None:
        parser.error("run requires --output")
    resource_policy = enforce_resource_policy()
    data, observed = validate_inputs(args)
    if args.mode == "estimate":
        result = estimate_result(data, observed, resource_policy)
    else:
        result = run_result(data, observed, resource_policy)
    if args.output is None:
        json.dump(result, sys.stdout, indent=2, sort_keys=True)
        sys.stdout.write("\n")
    else:
        atomic_write_json(args.output, result)


if __name__ == "__main__":
    main()
