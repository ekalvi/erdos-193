def completed_l8_owner_index():
    with (ROOT / "gate2-l7-construction-L8.pkl").open("rb") as handle:
        state8 = pickle.load(handle)
    gaps = len(state8["words"])
    starts = [0]
    for gap in range(gaps):
        starts.append(starts[-1] + len(state8["words"][gap]))
    assert starts[-1] + 1 == 311_738
    start_to_anchor = {start: index for index, start in enumerate(starts)}

    def owner_options(path_index):
        anchor_index = start_to_anchor.get(path_index)
        if anchor_index is not None:
            options = []
            if anchor_index > 0:
                options.append(anchor_index - 1)
            if anchor_index < gaps:
                options.append(anchor_index)
            assert options
            return tuple(options)
        gap = bisect.bisect_right(starts, path_index) - 1
        assert 0 <= gap < gaps
        assert starts[gap] < path_index < starts[gap + 1]
        return (gap,)

    return owner_options, starts


