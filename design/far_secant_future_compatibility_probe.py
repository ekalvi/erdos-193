#!/usr/bin/env python3
"""Exact bounded future-compatibility probe for the physical tagged tokens.

This program joins four pinned finite artifacts into the one correlated tree
that they actually describe::

    42 realized L7 parent corridors
       -> 146 realized L8 child corridors
          -> 488 initial-anchor L9 precursor corridors.

It is deliberately *not* an all-point-pair experiment.  The only deep
objects are the two physical source tokens already audited by
``deep_incidence_lineage.py``: one ordered pair of stable L7 endpoint
identities and one singleton stable L7 endpoint identity.  Their endpoints
are never split into independent address streams.  Every state retains the
actual selected L7/L8 connector word, its parent/slot address, and the
ordered tuple of successors generated by that one selected word.

For L8 the upstream deep artifact retained atom descriptions and exact mask
commitments but omitted raw bitsets.  ``run`` therefore rescans only the 151
requested (step, atom) objects over 40 connector domains.  It does not rescan
placed-point pairs.  The reconstructed raw bitsets are checked against every
upstream atom and aggregate channel commitment before they are used.

For L9 the precursor already contains reconstructible raw bitsets.  They are
decoded and checked before use.  The resulting local compatibility signature
has three overlapping components:

* direct: a candidate site on the carried tagged--tagged secant;
* endpoint: tagged-endpoint collision or a tagged endpoint on a
  candidate-internal line; and
* near-deep: a candidate site on a tagged-endpoint--placed-partner secant.

Near-deep masks are ORed exactly by partner birth age and by the base-three
shell of the exact joint owner radius.  L8 owner spans are the prefix-aware
pipeline spans stored by the deep audit.  L9 owner spans are reconstructed
from the exact completed-L8 block address and the initial-anchor owner tiles.
All ORs used by partition refinement are performed on raw integers, never on
mask hashes.  Hashes in the output are commitments to emitted compressed raw
payloads, not a substitute for semantic mask operations.

Bottom-up partition refinement is reported for channel-only,
birth/owner-shell, physical-partner, and literal-address keys.  A repeated
key with two distinct ordered successor colors is a finite falsifier of that
key on this tree.  Lack of such a disagreement is only non-falsification.

Critical omissions remain:

* there is no selected L9 connector word and no current-L9 connector prefix;
* L9 near-deep joins contain all 311,738 initial anchors but no current-L9
  connector interiors;
* only one recorded connector history is present; and
* no alternate-action Post closure, availability bound, contraction, or
  infinite safety certificate is attempted.

Run from the repository root on one low-priority thread::

    env OPENBLAS_NUM_THREADS=1 OMP_NUM_THREADS=1 MKL_NUM_THREADS=1 \
        VECLIB_MAXIMUM_THREADS=1 NUMEXPR_NUM_THREADS=1 \
        nice -n 15 python3 -B \
        design/far_secant_future_compatibility_probe.py estimate

    env OPENBLAS_NUM_THREADS=1 OMP_NUM_THREADS=1 MKL_NUM_THREADS=1 \
        VECLIB_MAXIMUM_THREADS=1 NUMEXPR_NUM_THREADS=1 \
        nice -n 15 python3 -B \
        design/far_secant_future_compatibility_probe.py run \
        --output /tmp/far-secant-future-compatibility-probe.json
"""

