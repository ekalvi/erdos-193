def l8_source_owner_profiles(source_gap, target_tile, endpoint_origins):
    definition = SOURCE_DEFINITIONS[source_gap]
    endpoints = []
    owner_options = []
    for stable_id in definition["tagged_endpoint_stable_ids"]:
        parsed = parse_stable_connector(stable_id)
        assert parsed["level"] == 7
        profile = endpoint_origins[stable_id]
        assert profile["birth_gap"] == parsed["gap"]
        options = (parsed["gap"],)
        owner_options.append(options)
        endpoints.append({
            "stable_id": stable_id,
            "birth_level": 7,
            "birth_age_at_observation": 1,
            "birth_gap": parsed["gap"],
            "interior_ordinal": parsed["ordinal"],
            "birth_parent_endpoint_stable_ids": profile[
                "birth_parent_endpoint_stable_ids"
            ],
            **owner_profile((options,), target_tile),
        })
    joint = owner_profile(owner_options, target_tile)
    return endpoints, joint


def validate_commitment(bits, domain_words, commitment):
    assert bits.bit_count() == commitment["words"]
    assert mask_sha256(bits, domain_words) == commitment["mask_sha256"]


