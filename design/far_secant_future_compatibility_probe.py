class ExactMaskStore:
    """Deduplicated, reconstructible exact bitset payloads."""

    def __init__(self):
        self.payloads = {}

    def record(self, bits, domain_words):
        raw = mask_raw(bits, domain_words)
        digest = hashlib.sha256(raw).hexdigest()
        reference = f"{domain_words}:{digest}"
        if reference not in self.payloads:
            compressed = zlib.compress(raw, level=9)
            self.payloads[reference] = {
                "domain_words": domain_words,
                "uncompressed_bytes": len(raw),
                "zlib_bytes": len(compressed),
                "encoding": (
                    "zlib+base64 fixed-length little-endian word bitset; "
                    "bit i is domain word i"
                ),
                "payload_base64": base64.b64encode(compressed).decode("ascii"),
            }
        return {
            "domain_words": domain_words,
            "killed_words": bits.bit_count(),
            "mask_sha256": digest,
            "exact_payload_ref": reference,
        }


def decode_input_mask(record, payloads):
    reference = record["exact_payload_ref"]
    payload = payloads[reference]
    assert payload["domain_words"] == record["domain_words"]
    raw = zlib.decompress(base64.b64decode(payload["payload_base64"]))
    assert len(raw) == payload["uncompressed_bytes"]
    assert hashlib.sha256(raw).hexdigest() == record["mask_sha256"]
    bits = int.from_bytes(raw, "little")
    assert bits.bit_count() == record["killed_words"]
    assert bits >> record["domain_words"] == 0
    return bits


