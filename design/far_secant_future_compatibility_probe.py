def requested_atom_inventory(deep):
    occurrences = deep_occurrences(deep)
    requested = defaultdict(dict)
    atom_occurrences = 0
    for record in occurrences:
        step = record["l8_step"]
        for atom in record["atoms"]:
            atom_occurrences += 1
            key = normalize(atom["atom_description"])
            previous = requested[step].get(key)
            commitment = atom["word_membership"]
            if previous is not None:
                assert previous == commitment
            requested[step][key] = commitment
    model_sizes = {
        record["step"]: record["domain_words"]
        for record in deep["all_domain_endpoint_incidence_closure"][
            "model_census"
        ]
    }
    census = {
        "effect_records": len(occurrences),
        "requested_atom_occurrences": atom_occurrences,
        "unique_step_atoms": sum(len(atoms) for atoms in requested.values()),
        "effective_steps": len(requested),
        "domain_words": sum(model_sizes[step] for step in requested),
    }
    assert census == EXPECTED_L8_RESCAN
    return requested, model_sizes, census


def rescan_l8_atom_masks(deep):
    requested, model_sizes, census = requested_atom_inventory(deep)
    result_buffers = {}
    target_indexes = {}
    for step in requested:
        site_targets = {
            atom[1]: atom for atom in requested[step]
            if atom[0] == "site"
        }
        line_targets = {
            atom[1]: atom for atom in requested[step]
            if atom[0] == "line"
        }
        assert len(site_targets) + len(line_targets) == len(requested[step])
        byte_count = (model_sizes[step] + 7) // 8
        result_buffers[step] = {
            atom: bytearray(byte_count) for atom in requested[step]
        }
        target_indexes[step] = (site_targets, line_targets)

    def scan_words(step, words, start_index, menu):
        site_targets, line_targets = target_indexes[step]
        buffers = result_buffers[step]
        for local_index, word in enumerate(words):
            word_index = start_index + local_index
            offsets = word_offsets(word, menu)
            if site_targets:
                for offset in set(offsets):
                    atom = site_targets.get(offset)
                    if atom is not None:
                        buffers[atom][word_index >> 3] |= 1 << (
                            word_index & 7
                        )
            if line_targets:
                lines = set()
                for left, first in enumerate(offsets):
                    for second in offsets[left + 1:]:
                        key = line_key(first, second)
                        if key is not None:
                            lines.add(key)
                for key in lines:
                    atom = line_targets.get(key)
                    if atom is not None:
                        buffers[atom][word_index >> 3] |= 1 << (
                            word_index & 7
                        )

    # Scan base domains while the base pickle is resident, then release it
    # before opening the much larger fragile extension.  This avoids holding
    # the two full pickle object graphs simultaneously.
    with (ROOT / "connector_domains4.pkl").open("rb") as handle:
        base = pickle.load(handle)
    menu = tuple(map(tuple, base["menu"]))
    index = {vector: ordinal for ordinal, vector in enumerate(menu)}
    base_sizes = {}
    for step in sorted(requested):
        words = sorted(base["domains"][step], key=len)
        base_sizes[step] = len(words)
        scan_words(step, words, 0, menu)
        del words
    del base

    fragile_counts = Counter()
    with (ROOT / "dstar5_fragile.pkl").open("rb") as handle:
        fragile = pickle.load(handle)
    for vector, words in fragile.items():
        step = index[tuple(vector)]
        if step not in requested:
            continue
        assert base_sizes[step] < FRAGILE_CUT
        indexed_words = (
            tuple(index[tuple(menu_step)] for menu_step in word)
            for word in words
        )
        scan_words(step, indexed_words, base_sizes[step], menu)
        fragile_counts[step] = len(words)
    del fragile

    result = {}
    per_step = []
    for step in sorted(requested):
        domain_words = base_sizes[step] + fragile_counts[step]
        assert domain_words == model_sizes[step]
        site_targets, line_targets = target_indexes[step]
        buffers = result_buffers.pop(step)
        for atom, buffer in buffers.items():
            bits = int.from_bytes(buffer, "little")
            commitment = requested[step][atom]
            assert bits.bit_count() == commitment["words"]
            assert mask_sha256(bits, domain_words) == commitment["mask_sha256"]
            result[(step, atom)] = bits
        per_step.append({
            "step": step,
            "base_domain_words": base_sizes[step],
            "fragile_extension_words": fragile_counts[step],
            "domain_words": domain_words,
            "requested_site_atoms": len(site_targets),
            "requested_line_atoms": len(line_targets),
        })
    assert not result_buffers
    return result, per_step, census


def mask_key(bits_by_shell):
    return tuple(sorted((normalize(key), bits) for key, bits in bits_by_shell.items()))


def output_mask_map(bits_by_key, domain_words, store):
    return [
        {
            "key": as_json(key),
            "mask": store.record(bits, domain_words),
        }
        for key, bits in sorted(bits_by_key.items())
    ]


