def file_sha256(path):
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1 << 20), b""):
            digest.update(block)
    return digest.hexdigest()


def as_json(value):
    if isinstance(value, tuple):
        return [as_json(item) for item in value]
    if isinstance(value, list):
        return [as_json(item) for item in value]
    if isinstance(value, dict):
        return {str(key): as_json(item) for key, item in value.items()}
    return value


def stable_bytes(value):
    return json.dumps(
        as_json(value), sort_keys=True, separators=(",", ":")
    ).encode("utf-8")


def stable_hash(value):
    return hashlib.sha256(stable_bytes(value)).hexdigest()


def mask_raw(bits, size):
    return bits.to_bytes((size + 7) // 8, "little")


def mask_sha256(bits, size):
    return hashlib.sha256(mask_raw(bits, size)).hexdigest()


def normalize(value):
    if isinstance(value, dict):
        return tuple(
            (normalize(key), normalize(item))
            for key, item in sorted(value.items(), key=lambda pair: str(pair[0]))
        )
    if isinstance(value, list):
        return tuple(normalize(item) for item in value)
    if isinstance(value, tuple):
        return tuple(normalize(item) for item in value)
    return value


def parse_stable_connector(stable_id):
    match = STABLE_CONNECTOR_RE.match(stable_id)
    if match is None:
        raise ValueError(f"not a connector stable id: {stable_id}")
    return {
        key: int(value) for key, value in match.groupdict().items()
    }


def base3_shell(distance):
    """0 for zero; j>=1 is least j with distance <= 3**(j-1)."""
    if distance < 0:
        raise ValueError("negative owner distance")
    if distance == 0:
        return 0
    shell = 1
    ceiling = 1
    while distance > ceiling:
        shell += 1
        ceiling *= 3
    return shell


def pareto_spans(owner_options, target_tile):
    """Exact nondominated signed (left,right) spans over joint owners."""
    spans = {(0, 0)}
    for options in owner_options:
        nxt = set()
        for left, right in spans:
            for owner in options:
                nxt.add((
                    max(left, target_tile - owner, 0),
                    max(right, owner - target_tile, 0),
                ))
        spans = {
            pair for pair in nxt
            if not any(
                other != pair
                and other[0] <= pair[0]
                and other[1] <= pair[1]
                for other in nxt
            )
        }
    return tuple(sorted(spans))


def owner_profile(owner_options, target_tile):
    owner_options = tuple(tuple(options) for options in owner_options)
    spans = pareto_spans(owner_options, target_tile)
    radius = min(max(span) for span in spans)
    return {
        "owner_options": as_json(owner_options),
        "target_tile": target_tile,
        "owner_pareto_spans": as_json(spans),
        "minimum_joint_owner_radius": radius,
        "minimum_joint_owner_base3_shell": base3_shell(radius),
    }


