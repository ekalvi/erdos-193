def partition_refinement(l7_raw, l8_raw, l9_raw):
    variants = (
        "channel",
        "birth_owner_shell",
        "physical_partner",
        "literal_address",
    )
    result = {}
    for variant in variants:
        l9_colors, l9_stats = assign_colors(
            l9_raw, lambda record: local_key(record, variant), "L9C"
        )
        l8_local_colors, l8_local_stats = assign_colors(
            l8_raw, lambda record: local_key(record, variant), "L8B"
        )

        def l8_outcome(record):
            return tuple(
                (
                    slot,
                    l9_raw[successor]["step"],
                    l9_colors[successor],
                )
                for slot, successor in enumerate(record["successor_ids"])
            )

        l8_refined_colors, l8_refined_stats = assign_colors(
            l8_raw,
            lambda record: (
                local_key(record, variant),
                record["actual_word"],
                l8_outcome(record),
            ),
            "L8R",
        )

        def l7_outcome(record):
            return tuple(
                (
                    slot,
                    l8_raw[successor]["step"],
                    l8_refined_colors[successor],
                )
                for slot, successor in enumerate(record["successor_ids"])
            )

        l7_base_colors, l7_base_stats = assign_colors(
            l7_raw, lambda record: l7_base_key(record, variant), "L7B"
        )
        l7_refined_colors, l7_refined_stats = assign_colors(
            l7_raw,
            lambda record: (
                l7_base_key(record, variant),
                record["actual_word"],
                l7_outcome(record),
            ),
            "L7R",
        )
        result[variant] = {
            "round_0_L9_exact_local_partition": l9_stats,
            "round_0_L8_exact_local_partition": l8_local_stats,
            "round_1_L8_ordered_future_refinement": l8_refined_stats,
            "round_1_L7_structural_base_partition": l7_base_stats,
            "round_2_L7_ordered_future_refinement": l7_refined_stats,
            "L8_transition_congruence_audit": transition_audit(
                l8_raw,
                lambda record: (
                    local_key(record, variant), record["actual_word"]
                ),
                l8_outcome,
            ),
            "L7_transition_congruence_audit": transition_audit(
                l7_raw,
                lambda record: (
                    l7_base_key(record, variant), record["actual_word"]
                ),
                l7_outcome,
            ),
            "color_stream_sha256": stable_hash({
                "L9": [
                    [as_json(identity), l9_colors[identity]]
                    for identity in sorted(l9_colors)
                ],
                "L8_local": [
                    [as_json(identity), l8_local_colors[identity]]
                    for identity in sorted(l8_local_colors)
                ],
                "L8_refined": [
                    [as_json(identity), l8_refined_colors[identity]]
                    for identity in sorted(l8_refined_colors)
                ],
                "L7_base": [
                    [as_json(identity), l7_base_colors[identity]]
                    for identity in sorted(l7_base_colors)
                ],
                "L7_refined": [
                    [as_json(identity), l7_refined_colors[identity]]
                    for identity in sorted(l7_refined_colors)
                ],
            }),
        }
    return result


